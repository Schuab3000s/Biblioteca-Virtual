    private void preencherTabela() {
        ConsultasJPA consultasJPA = new ConsultasJPA();

        List<Object[]> resultadosLivros = consultasJPA.consultarInformacoesEmprestimo();
        DefaultTableModel model = (DefaultTableModel) tblEmprestimo.getModel();

        model.setRowCount(0);

        for (Object[] row : resultadosLivros) {
            // Mapear os valores corretamente para as colunas da tabela
            String livro = (String) row[0];
            String cliente = (String) row[1];
            Date data = (Date) row[2];
            String emprestado = (String) row[3];

            // Adicionar a linha na tabela
            model.addRow(new Object[]{livro, cliente, data, emprestado});
        }
    }

    //m√©todo para deixar centralizado os dados na tabela
    private void centralizarConteudoTabela() {

        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

        for (int i = 0; i < tblEmprestimo.getColumnCount(); i++) {
            tblEmprestimo.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
    }

