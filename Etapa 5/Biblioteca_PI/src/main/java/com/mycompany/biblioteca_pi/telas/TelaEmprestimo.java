    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // Obt√©m o termo de busca digitado pelo usu√°rio
        String termoBusca = txtBuscarEmprestimo.getText().trim();

        // Verifica se o campo de busca n√£o est√° vazio
        if (!termoBusca.isEmpty()) {
            // Realiza a consulta com base no termo de busca
            ConsultasJPA consultasJPA = new ConsultasJPA();
            List<Object[]> resultadosBusca = consultasJPA.consultarInformacoesEmprestimo(termoBusca);

            // Atualiza a tabela com os resultados da busca
            DefaultTableModel model = (DefaultTableModel) tblEmprestimo.getModel();
            model.setRowCount(0); // Limpa as linhas existentes na tabela

            for (Object[] row : resultadosBusca) {
                model.addRow(row); // Adiciona cada linha de resultado √† tabela
            }
        } else {
            // Se o campo de busca estiver vazio, exibe uma mensagem de erro
            JOptionPane.showMessageDialog(this, "Por favor, digite um termo de busca.", "Campo de busca vazio", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

