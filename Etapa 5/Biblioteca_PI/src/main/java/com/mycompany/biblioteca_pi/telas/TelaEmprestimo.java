    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
        // Verifica se alguma linha da tabela est√° selecionada
        int linhaSelecionada = tblEmprestimo.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione um empr√©stimo na tabela.", "Nenhum empr√©stimo selecionado", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Obt√©m os detalhes do empr√©stimo selecionado
        String nomeLivro = (String) tblEmprestimo.getValueAt(linhaSelecionada, 0);
        String nomeCliente = (String) tblEmprestimo.getValueAt(linhaSelecionada, 1);
        Date dataEmprestimo = (Date) tblEmprestimo.getValueAt(linhaSelecionada, 2);
        String emprestado = (String) tblEmprestimo.getValueAt(linhaSelecionada, 3);

        EmprestimoJPA emprestimoJPA = new EmprestimoJPA();
        Integer idEmprestimo = emprestimoJPA.buscarIdEmprestimo(nomeLivro, nomeCliente, dataEmprestimo, emprestado);

        // Verifica se o ID do empr√©stimo foi encontrado
        if (idEmprestimo == null) {
            JOptionPane.showMessageDialog(this, "N√£o foi poss√≠vel encontrar o ID do empr√©stimo.", "ID n√£o encontrado", JOptionPane.ERROR_MESSAGE);
            return;
        }

        emprestimoJPA.devolverLivro(idEmprestimo);

        preencherTabela();
        centralizarConteudoTabela();

    }//GEN-LAST:event_btnDevolverActionPerformed

