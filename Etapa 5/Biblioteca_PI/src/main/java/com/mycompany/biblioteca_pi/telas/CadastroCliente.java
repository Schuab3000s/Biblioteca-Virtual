    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String telefone = txtTelefone.getText();
        String endereco = txtEndereco.getText();

        Cliente cliente = new Cliente(nome, cpf, telefone, endereco);
        ClienteJPA clienteJPA = new ClienteJPA(entityManager);

        boolean cadastrado = clienteJPA.adicionarCliente(cliente);

        if (cadastrado) {
            JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar cliente. Por favor, tente novamente.");
        }

        new Biblioteca(nome).setVisible(true);
        dispose();

    }//GEN-LAST:event_btnCadastrarActionPerformed

