    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // Verificar se foi selecionada alguma linha na tabela
        int selectedRow = tblLivros.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um livro para excluir.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Obter o ID do livro selecionado na tabela
        String nome_livro = (String) tblLivros.getValueAt(selectedRow, 0);

        // Confirmar se o usuário deseja realmente excluir o livro
        int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este livro?", "Confirmar exclusão", JOptionPane.YES_NO_OPTION);
        if (confirmacao == JOptionPane.YES_OPTION) {
            // Excluir o livro utilizando o método da classe LivrosJPA
            livroJPA.excluirLivro(nome_livro);

            // Recarregar os dados na tabela após a exclusão
            carregarDadosNaTabela();
            centralizarConteudoTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

