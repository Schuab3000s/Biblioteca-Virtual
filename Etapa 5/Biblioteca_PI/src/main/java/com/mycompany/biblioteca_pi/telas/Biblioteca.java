    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // Obter o texto inserido no campo de busca
        String termoBusca = txtBuscarLivro.getText().trim();

        // Verificar se o campo de busca está vazio
        if (termoBusca.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um termo de busca.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Instanciar a classe ConsultasJPA
        ConsultasJPA consultasJPA = new ConsultasJPA();

        // Realizar a consulta com base no termo de busca
        List<Object[]> resultadosBusca = consultasJPA.consultarInformacoesLivro(termoBusca);

        // Atualizar a tabela com os resultados da busca
        atualizarTabela(resultadosBusca);
    }//GEN-LAST:event_btnBuscarActionPerformed

