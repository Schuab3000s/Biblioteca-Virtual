    private void carregarDadosNaTabela() {
        ConsultasJPA consultasJPA = new ConsultasJPA();

        List<Object[]> resultadosLivros = consultasJPA.consultarInformacoesLivro();
        DefaultTableModel model = (DefaultTableModel) tblLivros.getModel();

        model.setRowCount(0);

        for (Object[] row : resultadosLivros) {
            // Mapear os valores corretamente para as colunas da tabela
            String livro = (String) row[0];
            String autor = (String) row[1];
            String genero = (String) row[2];
            Date data_lancamento = (Date) row[3];

            // Adicionar a linha na tabela
            model.addRow(new Object[]{livro, autor, genero, data_lancamento});
        }
    }

    //método para deixar centralizado os dados na tabela
    private void centralizarConteudoTabela() {

        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

        for (int i = 0; i < tblLivros.getColumnCount(); i++) {
            tblLivros.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
    }

    private void atualizarTabela(List<Object[]> resultados) {
        DefaultTableModel model = (DefaultTableModel) tblLivros.getModel();
        model.setRowCount(0); // Limpar todas as linhas da tabela

        for (Object[] row : resultados) {
            model.addRow(row); // Adicionar cada linha de resultado à tabela
        }
    }

