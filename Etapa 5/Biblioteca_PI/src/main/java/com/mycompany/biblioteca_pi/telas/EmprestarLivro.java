    private void preencherComboBox() {
        List<String> clientes = clienteJPA.listarCliente();
        ComboBoxCliente.removeAllItems(); // Limpa os itens existentes no JComboBox
        for (String cliente : clientes) {
            ComboBoxCliente.addItem(cliente); // Adiciona cada gênero ao JComboBox
        }

        List<String> livros = livroJPA.listarNomeLivros();
        ComboBoxLivro.removeAllItems();
        for (String livro : livros) {
            ComboBoxLivro.addItem(livro);
        }
    }

