    private void btnEmprestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmprestarActionPerformed

        EmprestimoJPA emprestimoJPA = new EmprestimoJPA();

        String nome_livro = ComboBoxLivro.getSelectedItem().toString();
        String nome_cliente = ComboBoxCliente.getSelectedItem().toString();
        String dataString = txtDataEmprestimo.getText().toString();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        Date data = null;
        try {
            data = dateFormat.parse(dataString);
        } catch (ParseException e) {
            e.printStackTrace();
        }
        int id_livro = emprestimoJPA.buscarId_LivroPorNome(nome_livro);
        int id_cliente = emprestimoJPA.buscarId_ClientePorNome(nome_cliente);

        Livro livro = new Livro();
        livro.setId_livro(id_livro);
        Cliente cliente = new Cliente();
        cliente.setId_cliente(id_cliente);

        Emprestimo emprestimo = new Emprestimo(livro, cliente, data, "sim");
        boolean cadastrado = emprestimoJPA.adicionarEmprestimo(emprestimo);

        if (cadastrado) {
            JOptionPane.showMessageDialog(this, "Empréstimo cadastrado com sucesso!");
            new TelaEmprestimo(usuario).setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar empréstimo. Por favor, tente novamente.");
        }

    }//GEN-LAST:event_btnEmprestarActionPerformed

