    public void cadastrar() {

        String nome = txtNome.getText();
        String data = txtNascimento.getText();
        String cpf = txtCpf.getText();
        String email = txtEmail.getText();

        boolean testeData = data.matches("[0-9]{2}[/][0-9]{2}[/][0-9]{4}");
        boolean testeCpf = cpf.matches("[0-9]{3}[.][0-9]{3}[.][0-9]{3}[-][0-9]{2}");

        if (nome.isEmpty() || data.isEmpty() || cpf.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else if (testeData == false) {
            JOptionPane.showMessageDialog(null, "Preencha corretamente o campo de Data\nNo formato: dd/mm/aaaa", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else if (testeCpf == false) {
            JOptionPane.showMessageDialog(null, "Preencha corretamente o campo de CPF\nNo formato: 000.000.000-00", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            Cliente cliente = new Cliente();
            cliente.setNome(nome);
            cliente.setCpf(cpf);
            cliente.setDataNascimento(data);
            cliente.setEmail(email);

            Listagem.Adicionar(cliente);

            TelaBiblioteca tela = new TelaBiblioteca();
            tela.setVisible(true);
            dispose();
        }
    }

